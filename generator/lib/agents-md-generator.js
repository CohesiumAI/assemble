/**
 * Assemble — Universal AGENTS.md Generator
 * Generates a standard AGENTS.md file for any platform.
 * Contains: build commands, agent table, conventions.
 */

const { agentId, marvelSlug, marvelDisplayName } = require('./template-engine');

/**
 * Generate a universal AGENTS.md content.
 * @param {Array} agents - Prepared agents array
 * @param {Array} workflows - Workflow array
 * @param {object} config - Project configuration
 * @returns {string} - Markdown content
 */
function generateUniversalAgentsMd(agents, workflows, config = {}) {
  let out = '# AGENTS.md — Assemble Agent Registry\n\n';
  out += '> Auto-generated by Assemble. Do not edit manually — regenerate with `npx create-assemble --update`.\n\n';

  // Build commands
  out += '## Build Commands\n\n';
  out += '```bash\n';
  out += 'npx create-assemble          # Install / configure\n';
  out += 'npx create-assemble --update # Regenerate from .assemble.yaml\n';
  out += 'assemble doctor              # Health check\n';
  out += 'assemble ls                  # List active agents/workflows\n';
  out += '```\n\n';

  // Agent table
  out += `## Agents (${agents.length})\n\n`;
  out += '| # | Agent ID | Marvel Name | Domain | @mention |\n';
  out += '|---|----------|-------------|--------|----------|\n';

  agents.forEach((agent, i) => {
    const id = agentId(agent);
    const slug = marvelSlug(agent);
    const display = marvelDisplayName(agent);
    const desc = (agent.meta.description || '').split('—')[0].trim() || '—';
    out += `| ${i + 1} | \`${id}\` | ${display} | ${desc} | \`@${slug}\` |\n`;
  });
  out += '\n';

  // Workflows
  if (workflows.length > 0) {
    out += `## Workflows (${workflows.length})\n\n`;
    out += '| Workflow | Trigger |\n';
    out += '|---------|--------|\n';
    for (const wf of workflows) {
      const raw = wf.raw || '';
      const nameMatch = raw.match(/name:\s*"?([^"\n]+)"?/);
      const name = nameMatch ? nameMatch[1].trim() : wf.fileName;
      const triggerMatch = raw.match(/trigger:\s*\/?([\w-]+)/);
      const trigger = triggerMatch ? `/${triggerMatch[1]}` : '—';
      out += `| ${name} | \`${trigger}\` |\n`;
    }
    out += '\n';
  }

  // Conventions
  out += '## Conventions\n\n';
  out += '- Use `/go <request>` for Jarvis smart routing\n';
  out += '- Use `@marvel-name` to invoke an agent directly\n';
  out += '- Use `/party <request>` for multi-agent sessions\n';
  out += '- Use `/dismiss` to end a session\n';
  out += `- Deliverables go to \`${config.output_dir || './assemble-output'}\`\n`;

  return out;
}

module.exports = { generateUniversalAgentsMd };
