#!/usr/bin/env node

/**
 * Assemble — Doctor
 * Health check (and optional auto-repair) for an Assemble installation.
 *
 * Usage:
 *   npx cohesiumai-assemble doctor [--project <path>] [--fix]
 *   node bin/doctor.js [--project <path>] [--fix]
 *
 * --fix   Automatically repair fixable issues (create missing dirs, files, etc.)
 */

const fs = require('fs');
const path = require('path');
const { execFileSync } = require('child_process');

// ─── All 21 supported platforms ────────────────────────────────────────────

const PLATFORMS_IDE = [
  'cursor', 'windsurf', 'cline', 'roocode', 'copilot', 'kiro',
  'trae', 'antigravity', 'codebuddy', 'crush', 'iflow',
  'kilocoder', 'opencode', 'qwencoder', 'rovodev', 'claude-code-desktop',
];

const PLATFORMS_CLI = [
  'claude-code', 'codex', 'gemini-cli', 'auggie', 'pi',
];

const ALL_PLATFORMS = [...PLATFORMS_IDE, ...PLATFORMS_CLI];

// ─── Config loader ─────────────────────────────────────────────────────────

const configLoaderPath = path.join(__dirname, '..', 'generator', 'lib', 'config-loader.js');
let loadConfig;
try {
  ({ loadConfig } = require(configLoaderPath));
} catch {
  // Fallback: inline minimal parser if config-loader is unavailable
  loadConfig = function fallbackLoadConfig(configPath) {
    if (!configPath || !fs.existsSync(configPath)) return {};
    const raw = fs.readFileSync(configPath, 'utf-8').replace(/\r\n/g, '\n').replace(/\r/g, '\n');
    const config = {};
    for (const line of raw.split('\n')) {
      const match = line.match(/^(\w[\w_]*):\s*(.+)$/);
      if (!match) continue;
      const [, key, value] = match;
      if (value.startsWith('[')) {
        config[key] = value.slice(1, -1).split(',').map(v => v.trim().replace(/["']/g, ''));
      } else {
        const cleaned = value.replace(/["']/g, '').trim();
        if (cleaned === 'true') config[key] = true;
        else if (cleaned === 'false') config[key] = false;
        else if (cleaned === 'all') config[key] = 'all';
        else config[key] = cleaned;
      }
    }
    return config;
  };
}

// ─── Template contents for fixable files ───────────────────────────────────

const MEMORY_TEMPLATE = `# Assemble — Cross-Session Memory

<!-- Auto-generated by doctor --fix -->

## Active Context

_No active context yet._

## Key Decisions

_No decisions recorded yet._

## Session Log

<!-- Append session entries below this line -->
`;

const METRICS_TEMPLATE = `# Assemble — Workflow Metrics

<!-- Auto-generated by doctor --fix -->

| Workflow | Started | Completed | Duration | Steps | Agents | Status |
|----------|---------|-----------|----------|-------|--------|--------|
`;

// ─── Doctor core ───────────────────────────────────────────────────────────

function doctor(projectDir, fixMode) {
  const checks = [];
  let ok = 0;
  let warn = 0;
  let fail = 0;
  let fixed = 0;

  /**
   * Register a health check.
   *
   * @param {string}   name     Human-readable label for the check
   * @param {Function} fn       Returns true (pass), 'warn' (warning), or string (failure detail)
   * @param {object}   [opts]   Optional: { fixable: true, fix: () => void }
   */
  function check(name, fn, opts = {}) {
    try {
      const result = fn();
      if (result === true) {
        checks.push({ name, status: 'ok' });
        ok++;
      } else if (result === 'warn') {
        checks.push({ name, status: 'warn' });
        warn++;
      } else {
        // Failure — attempt auto-fix if --fix is active and a fix function exists
        if (fixMode && opts.fixable && typeof opts.fix === 'function') {
          try {
            opts.fix();
            checks.push({ name, status: 'fixed', detail: result });
            fixed++;
          } catch (fixErr) {
            checks.push({ name, status: 'fail', detail: `${result} (fix failed: ${fixErr.message})` });
            fail++;
          }
        } else {
          const fixHint = opts.fixable ? ' [fixable with --fix]' : '';
          checks.push({ name, status: 'fail', detail: (result || 'Check failed') + fixHint });
          fail++;
        }
      }
    } catch (e) {
      checks.push({ name, status: 'fail', detail: e.message });
      fail++;
    }
  }

  // ─── Load config once (shared across checks) ──────────────────────────

  const configPath = path.join(projectDir, '.assemble.yaml');
  const configExists = fs.existsSync(configPath);
  const config = configExists ? loadConfig(configPath) : {};
  const outputDir = config.output_dir || './assemble-output';
  const resolvedOutputDir = path.resolve(projectDir, outputDir);
  const configuredPlatforms = Array.isArray(config.platforms) ? config.platforms : [];
  const governance = config.governance || 'none';
  const memory = config.memory === true || config.memory === 'true';
  const metrics = config.metrics === true || config.metrics === 'true';

  // ─── 1. Node.js >= 18 ─────────────────────────────────────────────────

  check('Node.js >= 18', () => {
    const major = parseInt(process.versions.node.split('.')[0], 10);
    return major >= 18 ? true : `Found v${process.versions.node}`;
  });

  // ─── 2. .assemble.yaml exists ─────────────────────────────────────────

  check('.assemble.yaml exists', () => {
    return configExists ? true : 'Missing — run npx cohesiumai-assemble';
  });

  // ─── 3. .assemble.yaml is parseable (has version + platforms) ─────────

  check('.assemble.yaml is parseable', () => {
    if (!configExists) return 'warn';
    const hasVersion = config.version !== undefined;
    const hasPlatforms = config.platforms !== undefined;
    return hasVersion && hasPlatforms ? true : 'Missing required fields (version, platforms)';
  });

  // ─── 4. Output directory exists ───────────────────────────────────────

  check('Output directory exists', () => {
    if (!configExists) return 'warn';
    return fs.existsSync(resolvedOutputDir) ? true : `Missing: ${outputDir}`;
  }, {
    fixable: true,
    fix: () => {
      fs.mkdirSync(resolvedOutputDir, { recursive: true });
    },
  });

  // ─── 5. Generated platform files exist for each configured platform ───

  check('Generated platform files exist', () => {
    if (!configExists) return 'warn';
    if (configuredPlatforms.length === 0) return 'warn';

    // Map each platform to at least one signature file/dir that should exist
    const platformSignatures = {
      'cursor':             ['.cursorrules', '.cursor'],
      'windsurf':           ['.windsurfrules', '.windsurf'],
      'cline':              ['.clinerules', '.cline'],
      'roocode':            ['.roomodes', '.roo'],
      'copilot':            ['.github/copilot-instructions.md', '.github'],
      'kiro':               ['.kiro'],
      'trae':               ['.trae'],
      'antigravity':        ['.antigravity'],
      'codebuddy':          ['.codebuddy'],
      'crush':              ['.crush'],
      'iflow':              ['.iflow'],
      'kilocoder':          ['.kilocoder'],
      'opencode':           ['.opencode'],
      'qwencoder':          ['.qwencoder'],
      'rovodev':            ['.rovodev'],
      'claude-code-desktop': ['.claude'],
      'claude-code':        ['CLAUDE.md', '.claude'],
      'codex':              ['AGENTS.md', 'codex.md'],
      'gemini-cli':         ['GEMINI.md', '.gemini'],
      'auggie':             ['.augment'],
      'pi':                 ['SYSTEM.md'],
    };

    const missing = [];
    for (const platform of configuredPlatforms) {
      const sigs = platformSignatures[platform];
      if (!sigs) continue;
      const found = sigs.some(sig => fs.existsSync(path.join(projectDir, sig)));
      if (!found) missing.push(platform);
    }

    if (missing.length === 0) return true;
    return `Missing files for: ${missing.join(', ')}`;
  }, {
    fixable: true,
    fix: () => {
      // Trigger full regeneration via generate.js --update
      const generatorPath = path.join(__dirname, '..', 'generator', 'generate.js');
      if (fs.existsSync(generatorPath)) {
        execFileSync(process.execPath, [
          generatorPath, '--update', '--project', projectDir,
        ], { stdio: 'pipe' });
      } else {
        throw new Error('generate.js not found');
      }
    },
  });

  // ─── 6. Agent source files exist ──────────────────────────────────────

  check('Agent source files (AGENT-*.md)', () => {
    const agentsDir = path.join(__dirname, '..', 'src', 'agents');
    if (!fs.existsSync(agentsDir)) return 'warn';
    const files = fs.readdirSync(agentsDir).filter(f => f.startsWith('AGENT-') && f.endsWith('.md'));
    return files.length > 0 ? true : 'No AGENT-*.md files found in src/agents/';
  });

  // ─── 7. Skill source files exist ──────────────────────────────────────

  check('Skill source files exist', () => {
    const skillsDir = path.join(__dirname, '..', 'src', 'skills');
    if (!fs.existsSync(skillsDir)) return 'warn';
    const shared = path.join(skillsDir, 'shared');
    const specific = path.join(skillsDir, 'specific');
    let count = 0;
    if (fs.existsSync(shared)) count += fs.readdirSync(shared).filter(f => f.endsWith('.md')).length;
    if (fs.existsSync(specific)) count += fs.readdirSync(specific).filter(f => f.endsWith('.md')).length;
    return count > 0 ? true : 'No skill .md files found in src/skills/';
  });

  // ─── 8. Rules files exist (routing.md, orchestrator.md, teams.md) ─────

  check('Rules files exist', () => {
    // Check generated rules in the project (claude-code style: .claude/rules/)
    const rulesDir = path.join(projectDir, '.claude', 'rules');
    const requiredRules = ['routing.md', 'orchestrator.md', 'teams.md'];

    if (!fs.existsSync(rulesDir)) {
      // Fallback: check source orchestrator
      const srcOrch = path.join(__dirname, '..', 'src', 'orchestrator', 'ORCHESTRATOR.md');
      return fs.existsSync(srcOrch) ? true : 'No rules files found';
    }

    const missing = requiredRules.filter(f => !fs.existsSync(path.join(rulesDir, f)));
    return missing.length === 0 ? true : `Missing rules: ${missing.join(', ')}`;
  });

  // ─── 9. Governance files exist (if governance != "none") ──────────────

  check('Governance files', () => {
    if (governance === 'none') return true;
    const govDir = path.join(projectDir, '.claude', 'rules', 'governance');
    const govFile = path.join(govDir, 'governance.md');
    if (fs.existsSync(govFile)) return true;
    return `Governance is "${governance}" but governance.md is missing`;
  }, {
    fixable: true,
    fix: () => {
      const govDir = path.join(projectDir, '.claude', 'rules', 'governance');
      fs.mkdirSync(govDir, { recursive: true });
      // Create a minimal governance file if none exists
      const govFile = path.join(govDir, 'governance.md');
      if (!fs.existsSync(govFile)) {
        const content = [
          '# Governance Rules',
          '',
          `Governance level: **${governance}**`,
          '',
          '## 1. Decision Gates',
          '',
          '| Complexity | Gate | Action |',
          '|------------|------|--------|',
          '| TRIVIAL | None | Agent acts autonomously |',
          '| MODERATE | Deliverable review | Produce deliverable, user validates |',
          '| COMPLEX | Phased approval | spec.md, plan.md, tasks.md with approval gates |',
          '',
          '## 2. Change Risk Assessment',
          '',
          '| Risk | Controls |',
          '|------|----------|',
          '| LOW | Agent acts, summary post-action |',
          '| MEDIUM | Plan required before action |',
          '| HIGH | Risk assessment + rollback plan + explicit approval |',
          '',
        ].join('\n');
        fs.writeFileSync(govFile, content, 'utf-8');
      }
    },
  });

  // ─── 10. _memory.md exists (if memory: true) ─────────────────────────

  check('Cross-session memory (_memory.md)', () => {
    if (!memory) return true;
    const memoryFile = path.join(resolvedOutputDir, '_memory.md');
    return fs.existsSync(memoryFile) ? true : '_memory.md missing';
  }, {
    fixable: true,
    fix: () => {
      fs.mkdirSync(resolvedOutputDir, { recursive: true });
      const memoryFile = path.join(resolvedOutputDir, '_memory.md');
      if (!fs.existsSync(memoryFile)) {
        fs.writeFileSync(memoryFile, MEMORY_TEMPLATE, 'utf-8');
      }
    },
  });

  // ─── 11. _metrics.md exists (if metrics: true) ────────────────────────

  check('Workflow metrics (_metrics.md)', () => {
    if (!metrics) return true;
    const metricsFile = path.join(resolvedOutputDir, '_metrics.md');
    return fs.existsSync(metricsFile) ? true : '_metrics.md missing';
  }, {
    fixable: true,
    fix: () => {
      fs.mkdirSync(resolvedOutputDir, { recursive: true });
      const metricsFile = path.join(resolvedOutputDir, '_metrics.md');
      if (!fs.existsSync(metricsFile)) {
        fs.writeFileSync(metricsFile, METRICS_TEMPLATE, 'utf-8');
      }
    },
  });

  // ─── 12. Custom agents directory (.assemble/agents/) ──────────────────

  check('Custom agents (.assemble/agents/)', () => {
    const dir = path.join(projectDir, '.assemble', 'agents');
    if (!fs.existsSync(dir)) return 'warn'; // Optional directory — warn, not fail
    const files = fs.readdirSync(dir).filter(f => f.endsWith('.md'));
    return files.length > 0 ? true : 'warn';
  }, {
    fixable: true,
    fix: () => {
      const dir = path.join(projectDir, '.assemble', 'agents');
      fs.mkdirSync(dir, { recursive: true });
    },
  });

  // ─── 13. Custom skills directory (.assemble/skills/) ──────────────────

  check('Custom skills (.assemble/skills/)', () => {
    const dir = path.join(projectDir, '.assemble', 'skills');
    if (!fs.existsSync(dir)) return 'warn'; // Optional directory — warn, not fail
    const files = fs.readdirSync(dir).filter(f => f.endsWith('.md'));
    return files.length > 0 ? true : 'warn';
  }, {
    fixable: true,
    fix: () => {
      const dir = path.join(projectDir, '.assemble', 'skills');
      fs.mkdirSync(dir, { recursive: true });
    },
  });

  // ─── 14. Windsurf workflows health (.md, frontmatter, system commands) ─

  check('Windsurf workflow format', () => {
    if (!configuredPlatforms.includes('windsurf')) return true;
    const wfDir = path.join(projectDir, '.windsurf', 'workflows');
    if (!fs.existsSync(wfDir)) return 'Missing .windsurf/workflows/';

    const files = fs.readdirSync(wfDir);
    const issues = [];

    // Detect residual .yaml files (broken pre-fix install)
    const yamlFiles = files.filter(f => f.endsWith('.yaml') || f.endsWith('.yml'));
    if (yamlFiles.length > 0) {
      issues.push(`${yamlFiles.length} residual .yaml file(s): ${yamlFiles.slice(0, 3).join(', ')}${yamlFiles.length > 3 ? '...' : ''}`);
    }

    // Check .md files have frontmatter
    const mdFiles = files.filter(f => f.endsWith('.md'));
    for (const f of mdFiles) {
      const content = fs.readFileSync(path.join(wfDir, f), 'utf-8');
      if (!content.startsWith('---\n') || !content.includes('description:')) {
        issues.push(`${f}: missing frontmatter (description:)`);
      }
    }

    // Check system commands exist
    const required = ['go.md', 'party.md', 'dismiss.md', 'help.md', 'doom.md'];
    const missing = required.filter(f => !files.includes(f));
    if (missing.length > 0) {
      issues.push(`Missing system commands: ${missing.join(', ')}`);
    }

    return issues.length === 0 ? true : issues.join('; ');
  }, {
    fixable: true,
    fix: () => {
      const wfDir = path.join(projectDir, '.windsurf', 'workflows');

      // 1. Remove residual .yaml files
      if (fs.existsSync(wfDir)) {
        for (const f of fs.readdirSync(wfDir)) {
          if (f.endsWith('.yaml') || f.endsWith('.yml')) {
            fs.unlinkSync(path.join(wfDir, f));
          }
        }
      }

      // 2. Trigger full regeneration to rebuild correct .md files
      const generatorPath = path.join(__dirname, '..', 'generator', 'generate.js');
      if (fs.existsSync(generatorPath)) {
        execFileSync(process.execPath, [
          generatorPath, '--update', '--project', projectDir,
        ], { stdio: 'pipe' });
      } else {
        throw new Error('generate.js not found');
      }
    },
  });

  // ─── 15. No orphaned platform files ───────────────────────────────────

  check('No orphaned platform files', () => {
    if (!configExists) return 'warn';

    // Known platform signature files/dirs at project root level
    const platformIndicators = {
      'cursor':             ['.cursorrules', '.cursor'],
      'windsurf':           ['.windsurfrules', '.windsurf'],
      'cline':              ['.clinerules', '.cline'],
      'roocode':            ['.roomodes', '.roo'],
      'copilot':            ['.github/copilot-instructions.md'],
      'kiro':               ['.kiro'],
      'trae':               ['.trae'],
      'antigravity':        ['.antigravity'],
      'codebuddy':          ['.codebuddy'],
      'crush':              ['.crush'],
      'iflow':              ['.iflow'],
      'kilocoder':          ['.kilocoder'],
      'opencode':           ['.opencode'],
      'qwencoder':          ['.qwencoder'],
      'rovodev':            ['.rovodev'],
      'claude-code-desktop': [], // shares .claude with claude-code
      'claude-code':        ['CLAUDE.md'],
      'codex':              ['AGENTS.md'],
      'gemini-cli':         ['GEMINI.md', '.gemini'],
      'auggie':             ['.augment'],
      'pi':                 ['SYSTEM.md'],
    };

    const orphaned = [];
    for (const [platform, indicators] of Object.entries(platformIndicators)) {
      if (configuredPlatforms.includes(platform)) continue;
      for (const indicator of indicators) {
        if (fs.existsSync(path.join(projectDir, indicator))) {
          orphaned.push(platform);
          break;
        }
      }
    }

    if (orphaned.length === 0) return true;
    return `Orphaned files for platforms not in config: ${orphaned.join(', ')}`;
  });

  // ─── 15. Config version matches package.json version ──────────────────

  check('Config version matches installed version', () => {
    if (!configExists) return 'warn';
    const configVersion = config.version;
    if (!configVersion) return 'warn';

    const pkgPath = path.join(__dirname, '..', 'package.json');
    if (!fs.existsSync(pkgPath)) return 'warn';

    try {
      const pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf-8'));
      const installedVersion = pkg.version;
      if (!installedVersion) return 'warn';

      // Normalize: strip surrounding quotes that may come from YAML parsing
      const normalized = configVersion.replace(/["']/g, '');
      return normalized === installedVersion
        ? true
        : `Config: ${normalized}, installed: ${installedVersion}`;
    } catch {
      return 'warn';
    }
  });

  // ─── Print results ────────────────────────────────────────────────────

  console.log('');
  console.log('🩺 Assemble Doctor' + (fixMode ? ' (--fix mode)' : '') + '\n');

  for (const c of checks) {
    let icon;
    switch (c.status) {
      case 'ok':    icon = '✅'; break;
      case 'warn':  icon = '⚠️ '; break;
      case 'fixed': icon = '🔧'; break;
      default:      icon = '❌'; break;
    }
    const suffix = c.detail ? ` — ${c.detail}` : '';
    console.log(`  ${icon} ${c.name}${suffix}`);
  }

  // Summary line
  const parts = [`${ok} passed`, `${warn} warnings`, `${fail} failures`];
  if (fixed > 0) parts.push(`${fixed} fixed`);
  console.log(`\n  ${parts.join(', ')}\n`);

  process.exit(fail > 0 ? 1 : 0);
}

// ─── Argument parsing ──────────────────────────────────────────────────────

const args = process.argv.slice(2);

// Remove the "doctor" subcommand if present (when invoked via cli.js)
const cleanArgs = args.filter(a => a !== 'doctor');

let projectDir = process.cwd();
const projIdx = cleanArgs.indexOf('--project');
if (projIdx >= 0 && cleanArgs[projIdx + 1]) {
  projectDir = path.resolve(cleanArgs[projIdx + 1]);
}

const fixMode = cleanArgs.includes('--fix');

doctor(projectDir, fixMode);
